    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if(this.cpf != ""){
            PessoaDAO pessoaDAO = new PessoaDAO();
            Pessoa pessoa = new Pessoa();
            pessoa = pessoaDAO.buscaPessoa(this.cpf);
            id_pes = pessoa.getId_pes();
            
            EnderecoDAO enderecoDAO = new EnderecoDAO();
            Endereco endereco = pessoa.getEndereco();
            
            AluguelDAO aluguelDAO = new AluguelDAO();
            Aluguel aluguel = new Aluguel();
            
            if (aluguelDAO.verificaAluguelC(id_pes) == true){
                JOptionPane.showMessageDialog(this, "O Cliente não pode ser excluído pois existe um aluguel ativo!");
                listarTabela("");
                DefaultTableModel valor = (DefaultTableModel) jTable1.getModel();
                valor.fireTableDataChanged();
            }else{
                pessoaDAO.deletar(pessoa);
                enderecoDAO.deletar(endereco);
                cpf = "";
                id_pes = 0;
                JOptionPane.showMessageDialog(this, "Registro Eliminado com Sucesso!");
                listarTabela("");
                DefaultTableModel valor = (DefaultTableModel) jTable1.getModel();
                valor.fireTableDataChanged();
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Nenhum registro selecionado!");
        }
        cpf = "";
    }//GEN-LAST:event_jButtonExcluirActionPerformed

