    public void listarTabela(String cpf){
        DefaultTableModel valor = (DefaultTableModel) jTable1.getModel();
        
        valor.getDataVector().removeAllElements();
        PessoaDAO pessoaDAO = new PessoaDAO();
        
        List<Pessoa> pessoas = pessoaDAO.listarPessoa(cpf);
        int i = 0;
        while(pessoas.size() > i){
            valor.addRow(new Object[]{String.valueOf(pessoas.get(i).getNome_pes()),
                                      String.valueOf(pessoas.get(i).getCpf())});
            i ++;
            
        }
    }
